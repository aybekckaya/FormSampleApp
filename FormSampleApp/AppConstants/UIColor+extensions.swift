import Foundation
import UIKit

extension UIColor {
  
    static func backgroundColor()->UIColor {
        return UIColor(red:245/255.0, green: 245/255.0, blue: 245/255.0, alpha: 1.0)
    }
    
    static func textfieldBGColor()->UIColor {
        return UIColor(red:247/255.0, green: 247/255.0, blue: 247/255.0, alpha: 1.0)
    }
    
    static func morhipoPurpleColor()->UIColor {
        return UIColor(red:113/255.0, green: 61/255.0, blue: 138/255.0, alpha: 1.0)
    }
    
    static func textfieldAlertRedColor()->UIColor {
        return UIColor(red:255/255.0, green: 1/255.0, blue: 1/255.0, alpha: 1.0)
    }
    
}
